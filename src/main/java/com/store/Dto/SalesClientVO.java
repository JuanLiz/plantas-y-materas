package com.store.Dto;

public class SalesClientVO {
	public long idclients;
	public String name;
	public double total;
	
	public long getIdclients() {
		return idclients;
	}
	public void setIdclients(long l) {
		this.idclients = l;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getTotal() {
		return total;
	}
	public void setTotal(double total) {
		this.total = total;
	}
	
	
}
